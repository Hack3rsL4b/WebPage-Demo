            <h2 class="title-home">¿Quiénes somos?</h2>
            <p>
                En SegurInfo Hack3rs L4b somos un equipo de investigación que busca aportar a la sociedad en temas de ciberseguridad para 
                ayudar a solventar problemáticas de interés general. Buscamos, en conjunto con la Universidad San Buenaventura, y partiendo 
                de la malla curricular, hacer un acercamiento al área investigativa para así dar paso a la creación de nuevas tecnologías 
                pertientes e innovadoras en el sector de las tecnologías de la información (TIC), Tecnologías en Salud (TS) y seguridad 
                informática, tanto a nivel social como corporativo.
            </p>
            <h2 class="title-home">Misión</h2>
            <p>
                El semillero de investigación SegurInfo Hack3rs L4b adscrito al grupo de Investigación SOLSYTEC (Soluciones en Sistemas
                y Tecnología) tiene como visión constituirse como una fuente de innovación, desarrollo e impulso para los estudiantes y
                docentes del programa de ingeniería de sistemas de la Universidad San Buenaventura Sede Bogotá, en temas relacionados con
                seguridad informática, seguridad de la información, seguridad de IT y, la cada vez mas importante, ciberseguridad de los
                datos, mediante la cooperación con otros semilleros de investigación, el desarrollo de proyectos de investigación y la
                participación en eventos académicos.
            </p>
            <h2 class="title-home">Visión</h2>
            <p>
                SegurInfo Hack3rs L4b se plantea como visión para el 2030, consolidar su participación en proyectos de investigación 
                enfocados al sector empresarial, la participación en eventos académicos de gran relevancia y la formación continua de 
                sus integrantes permitiendo la visibilización de la Universidad San Buenaventura Sede Bogotá y el programa de Ingeniería 
                de Sistemas como un programa con un alto nivel investigativo e innovador, con un alto impacto en la revolución industrial 
                4.0 y con particular énfasis en la seguridad informática y la ciberseguridad de los datos.
            </p>
